package beans;

import java.util.Date;

public class NotokBean {
	private int sn;
	private int pid;
	private Date bcdate;
private String qg;
	public int getSn() {
		return sn;
	}

	public void setSn(int sn) {
		this.sn = sn;
	}

	public int getPid() {
		return pid;
	}

	public void setPid(int pid) {
		this.pid = pid;
	}

	public Date getBcdate() {
		return bcdate;
	}

	public void setBcdate(Date bcdate) {
		this.bcdate = bcdate;
	}

	public String getQg() {
		return qg;
	}

	public void setQg(String qg) {
		this.qg = qg;
	}

	
}
