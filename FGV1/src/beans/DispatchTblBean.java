package beans;

public class DispatchTblBean {
	private int sn;
	private int pid;
	private String qg;
	public int getSn() {
		return sn;
	}
	public void setSn(int sn) {
		this.sn = sn;
	}
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	public String getQg() {
		return qg;
	}
	public void setQg(String qg) {
		this.qg = qg;
	}
}
